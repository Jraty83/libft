#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int i;
	int	j;

	i = 0;
	while (s1[i])
		i++;
	j = 0;
	while (s2[j] && n)
	{
		s1[i++] = s2[j++];
		n--;
	}
	s1[i] = '\0';
	return (s1);
}
